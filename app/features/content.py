"""Content Studio — DB-backed overrides for the application's hardcoded UI copy.

Every catalogued string (brand, navigation, page headings, subtitles, instructions)
can be overridden by an admin via the Content Studio ("Admin Change") without code
changes. Admins can also register *custom* source strings (any exact text they see
in the app) to override. Effective overrides are applied client-side by a text-node
replacement pass keyed on the exact default string, so an override reaches the string
wherever it is rendered. Reading the effective overrides is public (so login-screen and
pre-auth chrome localise too); editing requires the admin.content permission.
"""
